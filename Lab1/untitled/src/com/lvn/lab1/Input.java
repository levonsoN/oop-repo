package com.lvn.lab1;

class Input {
    private double x;
    private double a;
    private double b;

    public Input(double x, double a, double b) {
        this.x = x;
        this.a = a;
        this.b = b;
    }

    public double getX() {
        return x;
    }

    public double getA() {
        return a;
    }

    public double getB() {
        return b;
    }
}
